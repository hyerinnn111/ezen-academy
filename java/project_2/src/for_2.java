public class for_2 {
    public static void main(String[] args) {
        // for문을 써서 2,4,6,8,10 출력해 주세요
for (int i = 2; i <= 10; i = i + 2) {
    System.out.println(i);
}
        // for문을 써서 5,4,3,2,1 출력해 주세요
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
